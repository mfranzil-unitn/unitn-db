FROM Emp E
WHERE salary > (
    SELECT MAX(D.budget)
    FROM Dept D NATURAL JOIN Works W  -- nelle subquery le tabelle esterne sono ancora visibili!
    WHERE E.eid = W.eid
)

SELECT ename
FROM Emp E
WHERE salary > ALL(
    SELECT D.budget
    FROM Dept D NATURAL JOIN Works W 
    WHERE E.eid = W.eid
)

SELECT D.managerid
FROM Dept D
GROUP BY managerid
HAVING MIN(budget) > 1000000

SELECT E.ename
FROM Emp E JOIN Dept D ON managerid = eid
WHERE budget = (
    SELECT MAX(budget)
    FROM Dept D
)

SELECT managerid
FROM Dept D
GROUP BY managerid
HAVING SUM(budget) > 5000000

SELECT managerid
FROM (
    SELECT managerid, SUM(budget) as cb
    FROM Dept
    GROUP BY managerid
) AS ControlM
WHERE cb =  (
    SELECT MAX(cb)
    FROM ControlM
)

-- Data to create the database:

CREATE TABLE Emp (
    eid SERIAL NOT NULL PRIMARY KEY,
    ename TEXT,
    age INTEGER,
    salary REAL
);

CREATE TABLE Dept(
    did SERIAL NOT NULL PRIMARY KEY,
    dname TEXT,
    budget REAL,
    managerid SERIAL,
    FOREIGN KEY(managerid) REFERENCES Emp(eid)
);

CREATE TABLE Works(
    eid SERIAL,
    did SERIAL,
    pct_time INTEGER,
    FOREIGN KEY(eid) REFERENCES Emp,
    FOREIGN KEY(did) REFERENCES Dept,
    PRIMARY KEY(eid, did)
)

